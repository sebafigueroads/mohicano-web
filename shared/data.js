/* ========================================================
   Estado global compartido · cart · wishlist · recently viewed
   Persiste con localStorage entre las 3 versiones
   ======================================================== */

(function(){
  var LS = window.localStorage;
  var K = { cart:'mhc_cart', wish:'mhc_wish', recent:'mhc_recent' };

  function get(k){ try{ return JSON.parse(LS.getItem(k) || 'null') || []; }catch(e){ return []; } }
  function set(k,v){ try{ LS.setItem(k, JSON.stringify(v)); }catch(e){} document.dispatchEvent(new CustomEvent('mhc:state', {detail:{key:k, value:v}})); }

  window.MHC = {
    /* ---- CART ---- */
    cart(){ return get(K.cart); },
    addToCart(prodId, size, qty){
      qty = qty || 1;
      var cart = get(K.cart);
      var idx = cart.findIndex(function(i){ return i.id===prodId && i.size===size; });
      if(idx>=0) cart[idx].qty += qty;
      else cart.push({id:prodId, size:size, qty:qty, ts:Date.now()});
      set(K.cart, cart);
      return cart;
    },
    removeFromCart(prodId, size){
      var cart = get(K.cart).filter(function(i){ return !(i.id===prodId && i.size===size); });
      set(K.cart, cart);
      return cart;
    },
    updateCartQty(prodId, size, qty){
      var cart = get(K.cart);
      var idx = cart.findIndex(function(i){ return i.id===prodId && i.size===size; });
      if(idx>=0){
        if(qty<=0) cart.splice(idx,1);
        else cart[idx].qty = qty;
        set(K.cart, cart);
      }
      return cart;
    },
    cartCount(){
      return get(K.cart).reduce(function(s,i){ return s+i.qty; }, 0);
    },
    cartSubtotal(){
      var items = get(K.cart);
      var byId = {};
      (window.MOHICANO_DATA.products||[]).forEach(function(p){ byId[p.id]=p; });
      return items.reduce(function(s,i){
        var p = byId[i.id]; return p ? s + (p.price * i.qty) : s;
      }, 0);
    },
    clearCart(){ set(K.cart, []); },

    /* ---- WISHLIST ---- */
    wish(){ return get(K.wish); },
    toggleWish(prodId){
      var w = get(K.wish);
      var idx = w.indexOf(prodId);
      if(idx>=0) w.splice(idx,1); else w.push(prodId);
      set(K.wish, w);
      return w;
    },
    inWish(prodId){ return get(K.wish).indexOf(prodId)>=0; },

    /* ---- RECENTLY VIEWED ---- */
    recent(){ return get(K.recent); },
    trackView(prodId){
      var r = get(K.recent).filter(function(x){ return x!==prodId; });
      r.unshift(prodId);
      if(r.length>10) r = r.slice(0,10);
      set(K.recent, r);
      return r;
    },

    /* ---- HELPERS ---- */
    formatCLP(n){
      return '$' + Math.round(n).toString().replace(/\B(?=(\d{3})+(?!\d))/g, '.');
    },
    productById(id){
      return (window.MOHICANO_DATA.products||[]).find(function(p){ return p.id===id; });
    }
  };
})();
